#include "libft.h"

int	ft_isalnum(int ar)
{
	if ((ar >= '0' && ar <= '9') || (ar >= 'A' && ar <= 'Z') || \
	(ar >= 'a' && ar <= 'z'))
		return (1);
	else
		return (0);
}

