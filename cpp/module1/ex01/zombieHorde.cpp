#include "Zombie.hpp"

Zombie* zombieHorde(int N, std::string name)
{
	Zombie	*horde;
	int	i;

	if (N < 0)
		return (NULL);
	horde = new Zombie[N + 1]();
	if (!horde)
		return (NULL);
	i = 0;
	while (i != N)
	{
		horde[i].set(name, false);
		++i;
	}
	return (horde);
}
